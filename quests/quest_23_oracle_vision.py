def calculate_area(length, width):
    area = length * width
    return area

# Rectangle 1: A grand throne room
room_area = calculate_area(12, 8)
print(f"Throne Room (12 x 8): {room_area} sq units")

# Rectangle 2: A narrow dungeon cell
cell_area = calculate_area(4, 3)
print(f"Dungeon Cell (4 x 3): {cell_area} sq units")

